    private void btnAddAttributeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddAttributeActionPerformed
        String ref = txtRef.getText();
        if (!ref.matches("^(\\w+\\={0,1}\\w*\\;)*$")) {
            JOptionPane.showMessageDialog(null, "Ref is invalid", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        String attributes = "";
        if (!ref.isBlank()) {
            attributes += "ref='" + ref + "', ";
        }
        if (cbbUse.getSelectedIndex() > 0) {
            attributes += "use='" + cbbUse.getSelectedItem().toString() + "', ";
        }
        if (cbbType.getSelectedIndex() > 0) {
            attributes += "type='" + cbbType.getSelectedItem().toString() + "', ";
        }
        if (cbbFormat.getSelectedIndex() > 0) {
            attributes += "format='" + cbbFormat.getSelectedItem().toString() + "', ";
        }
        if (!attributes.isBlank()) {
            attributes = attributes.substring(0, attributes.length() - 2);
        }

        this.attributes = attributes;
        this.isOK = true;
        this.setVisible(false);
    }//GEN-LAST:event_btnAddAttributeActionPerformed

