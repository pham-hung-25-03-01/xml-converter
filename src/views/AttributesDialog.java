    private void loadCBB()
    {
        List<UseType> lstUse = Arrays.asList(Validator.UseType.values());
        for(UseType valueType : lstUse)
        {
            cbbUse.addItem(valueType.toString().toLowerCase());
        }
        
        List<ValueType> lstType = Arrays.asList(Validator.ValueType.values());
        for(ValueType valueType : lstType)
        {
            cbbType.addItem(valueType.toString().toLowerCase());
        }
        
        List<FormatType> lstFormat = Arrays.asList(Validator.FormatType.values());
        for(FormatType valueType : lstFormat)
        {
            cbbFormat.addItem(valueType.toString().toLowerCase());
        }
    }

